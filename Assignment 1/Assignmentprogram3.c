void Display()
{
    int i = 5;

    while(i >= 1)
    {
        printf("%d\t",i);
        i--;
    }
}

//////////////////////////////////////////////////////////////////
//
//      ENTRY POINT FUNCTION FOR APPLICATION
//
//////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0 ;
}
